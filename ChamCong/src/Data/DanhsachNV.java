    private void displayNhanvienData() {
        try (Connection conn = DriverManager.getConnection(ConnectDB.getURL(), ConnectDB.getUSER(), ConnectDB.getPWD())) {
            List<Nhanvien> nhanvienList = ConnectDB.getAllNhanvien(conn);

            DefaultTableModel model = (DefaultTableModel) tb_nv.getModel();
            model.setRowCount(0); 

            for (Nhanvien nhanvien : nhanvienList) {
                Object[] row = {
                    nhanvien.getMaNV(),
                    nhanvien.getName(),
                    nhanvien.getBirth(),
                    nhanvien.getChucvu(),
                    nhanvien.getPhone()
                    
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            System.err.println("Error retrieving data from database: " + e.getMessage());
        }
    }
